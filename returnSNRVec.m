function snrVec = returnSNRVec(gamma_bar1)


Hrd_e = (randn(1,1) + 1j*randn(1,1))*sqrt(gamma_bar1/2);
e2 = (randn(1,1) + 1j*randn(1,1))*(gamma_bar1/2);
Hrd = Hrd_e + e2;
snrVec = abs(Hrd')^2; 


end























































































































































































































































































































































































































































































































































































































